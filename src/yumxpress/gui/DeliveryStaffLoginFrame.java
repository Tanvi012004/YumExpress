    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        if(validateInputs()){
            try {
                String emailId=txtEmail.getText().trim();
                String password=String.valueOf(txtPassword.getPassword()).trim();
                password=PasswordEncryption.getEncryptedPassword(password);
                StaffPojo sp=StaffDAO.validateStaff(emailId, password);
                if (sp==null) {
                    JOptionPane.showMessageDialog(null, "Invalid EmailId/Password..! Try again..!");
                    return;
                }
                UserProfile.setEmailId(sp.getEmailId());
                UserProfile.setUserId(sp.getStaffId());
                UserProfile.setUserName(sp.getStaffName());
                new DeliveryStaffOptionFrame().setVisible(true);
                this.dispose();
            }
            catch(SQLException se){
                JOptionPane.showMessageDialog(null, "DB error in DeliveryStaffFrame");
                se.printStackTrace();
                return;
            }    
        }
        else{
            JOptionPane.showMessageDialog(null, "EmailId or Password cannot be left blank");
        }
    }//GEN-LAST:event_btnLoginActionPerformed

